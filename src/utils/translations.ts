// Utility: get translated product name
export function getProductName(name: string, nameTa: string | undefined, lang: Language): string {
  if (lang === 'en') return name;
  if (nameTa) return nameTa;
  return PRODUCT_NAMES_TA[name.toLowerCase()] || name;
}

// Utility: get translated customer name
export function getCustomerName(name: string, nameTa: string | undefined, lang: Language): string {
  if (lang === 'en') return name;
  if (nameTa) return nameTa;
  return name; // fallback to original if no Tamil name set
}

